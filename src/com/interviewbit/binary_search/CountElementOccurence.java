    public static void binarySearch(int[] arr, int low, int high, int numberWeAreSearching) {
        if (low <= high) {
            int mid = low + (high - low) / 2;

            if (arr[mid] == numberWeAreSearching) {
                totalOccurrence++;
                if (mid > 0 && arr[mid - 1] == numberWeAreSearching) {
                    binarySearch(arr, low, mid - 1, numberWeAreSearching);
                }
                if (mid < high && arr[mid + 1] == numberWeAreSearching) {
                    binarySearch(arr, mid + 1, high, numberWeAreSearching);
                }
            } else if (arr[mid] < numberWeAreSearching) {
                binarySearch(arr, mid + 1, high, numberWeAreSearching);
            } else if (arr[mid] > numberWeAreSearching) {
                binarySearch(arr, low, mid - 1, numberWeAreSearching);
            }
        }
    }
